export type Locale = keyof typeof languages;
type TranslationKey = keyof (typeof languages)["en-us"];

export const availableLocales = Object.keys(languages) as Locale[];
export const defaultLocale: Locale = "zh-cn";

export function translate(key: string, ...values: Array<string | number>) {
  const dictionary = languages[defaultLocale] ?? languages["en-us"];
  const template =
    dictionary[key as TranslationKey] ??
    languages["en-us"][key as TranslationKey] ??
    key;

  return values.reduce<string>(
    (text, value) => text.replace("%d", String(value)),
    template,
  );
}
